    private void StartTimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartTimerActionPerformed
            try {
            // If paused, just resume the timer
            if (isPaused && countdownTimer != null) {
                countdownTimer.start();
                isPaused = false;
                return;
            }

            // Otherwise, this is a fresh start â€” stop old timer
            if (countdownTimer != null) {
                countdownTimer.stop();
                countdownTimer = null;
            }

            // Read input fields only on fresh start
            int hours = Integer.parseInt(HoursField.getText().trim());
            int minutes = Integer.parseInt(MinutesField.getText().trim());
            int seconds = Integer.parseInt(SecondsField.getText().trim());

            remainingSeconds = (hours * 3600) + (minutes * 60) + seconds;

            if (remainingSeconds <= 0) {
                JOptionPane.showMessageDialog(this, "Enter a time greater than 0.");
                return;
            }

            // Lock fields
            HoursField.setEditable(false);
            MinutesField.setEditable(false);
            SecondsField.setEditable(false);

            countdownTimer = new Timer(1000, new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    if (remainingSeconds > 0) {
                        remainingSeconds--;

                        int hrs = remainingSeconds / 3600;
                        int mins = (remainingSeconds % 3600) / 60;
                        int secs = remainingSeconds % 60;

                        TimerCount.setText(String.format("%02d:%02d:%02d", hrs, mins, secs));
                    } else {
                        countdownTimer.stop();
                        new TimesUp().setVisible(true);
                        isPaused = false;
                    }
                }
            });

            countdownTimer.start();
            isPaused = false;

        } catch (NumberFormatException ex){
            new EnterValidValues().setVisible(true);
        }
    }//GEN-LAST:event_StartTimerActionPerformed

    private void ResetTimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetTimerActionPerformed
        if (countdownTimer != null) {
            countdownTimer.stop();
            countdownTimer = null;
        }
        
        isPaused = false;
        
        // Reset UI
        TimerCount.setText("00:00:00");
        HoursField.setText("0");
        MinutesField.setText("0");
        SecondsField.setText("0");

        HoursField.setEditable(true);
        MinutesField.setEditable(true);
        SecondsField.setEditable(true);
    }//GEN-LAST:event_ResetTimerActionPerformed

